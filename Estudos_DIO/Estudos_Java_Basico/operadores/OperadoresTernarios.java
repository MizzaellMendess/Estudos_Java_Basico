package operadores;

public class OperadoresTernarios {
    public static void main(String[] args) {
        //Operadores ternários ?, :
        //Os opradores ternarios permitem que seja feito um IF ELSE em uma única linha
        int a, b;
        a = 5;
        b = 7;

        //Se A for igual a B, então, o resultado após o simbolo ? será imprimido, porém se não for, então, o resultado após o : será imprimido
        String igual = a == b ? "verdadeiro" : "falso";
    }

}

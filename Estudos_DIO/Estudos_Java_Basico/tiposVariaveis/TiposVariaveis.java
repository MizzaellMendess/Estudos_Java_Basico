package tiposVariaveis;

public class TiposVariaveis {
        //Tipos primitivos

        //O tipo byte reserva 1 byte na memoria para armazenar o valor
        byte idade = 123;

        //O tipo short armazena 2 bytes
        short ano = 2024;

        //O tipo int armazena 4 bytes. Ã‰ o tipo mais comum para numeros inteiros
        int cep = 21070333;

        //O tipo long armazena 8 bytes. Deve ser adicionado um L no final do valor
        long cpf = 98765432109L;

        //O tipo float armazena 4 bytes, podendo armazenar ponto flutuante. Deve ser adicionado um F no final do valor
        float pi = 3.14F;

        //O tipo double armazena 8 bytes, podendo armazenar ponto flutuante
        double salarioMinimo = 1275.33;

        //O tipo char armazena caracteres
        char genero = 'M';
}
